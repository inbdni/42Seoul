int		ft_sqrt(int nb)
{
	int i;
	int x;

	if (nb <= 0)
		return (0);
	i = 0;
	x = 1;
	while (i++ < 20)
		x = (x + nb / x) / 2;
	if (x * x == nb)
		return (x);
	return (0);
}
